function add(m, n){
    return m + n;
}

function subtract(m, n){
    return m - n;
}

function multiply(m, n){
    return m* n;
}

function divide(m, n){
    return m/n;
}

function increment(m){
    return ++n;
}

function decrement(m){
    return --n;
}
function makeInt(string){
    return parseInt(string, 10);
}

function preserveDecimal(string){
    return parseFloat(string, 10);
}

